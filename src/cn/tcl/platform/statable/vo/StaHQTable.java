package cn.tcl.platform.statable.vo;

public class StaHQTable {
	private String center;//业务中心
	private String countryId;//国家ID
	private String country;//国家名称
	private String qty;//销售数量
	private String yar;//销售年份
	private String moth;//销售月份
	public String getCenter() {
		return center;
	}
	public void setCenter(String center) {
		this.center = center;
	}
	public String getCountryId() {
		return countryId;
	}
	public void setCountryId(String countryId) {
		this.countryId = countryId;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getQty() {
		return qty;
	}
	public void setQty(String qty) {
		this.qty = qty;
	}
	public String getMoth() {
		return moth;
	}
	public void setMoth(String moth) {
		this.moth = moth;
	}
	public String getYar() {
		return yar;
	}
	public void setYar(String yar) {
		this.yar = yar;
	}
}
